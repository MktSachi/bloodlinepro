        <h2>The Evolution of Blood Transfusion: A Journey Through Time</h2>
        <p>Blood is a vital fluid that circulates throughout the body, delivering essential 
        substances like oxygen and nutrients to cells while also removing waste products. </p>
  
        <p>The story begins in 1628 with William Harvey, 
        the first physician to describe the circulation of blood in the human body.
        His groundbreaking work laid the foundation for future advances in blood transfusion. 
        A few decades later, in 1665, Dr. Richard Lower performed the first successful blood 
        transfusion using dogs as both the donor and recipient.This pioneering experiment 
        set the stage for further exploration into the potential of transfusions.</p>  
        
        <p>Despite the initial success in animals, early human blood transfusions were fraught 
        with challenges. Many patients died after receiving transfusions, and the reason 
        remained a mystery until 1900. It was in this year that Dr. Karl Landsteiner made 
        a monumental discovery: the ABO blood group system. Landsteiner identified four main 
        blood types—O, A, B, and AB—and recognized the importance of matching blood types 
        between donors and recipients. His work earned him the Nobel Prize and revolutionized
        the field of blood transfusion.</p>
  
