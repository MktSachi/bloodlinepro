            <h2>A. Transportation</h2>
                <p>1. Respiratory Gases</p>
                  <p>Blood transports oxygen from the lungs to the body's cells, binding
                        it to hemoglobin in red blood cells. It also carries carbon dioxide, 
                        a waste product of cellular metabolism, back to the lungs for exhalation.</p>
                        <img src="image/repository gases.jpg" alt="Respiratory Gases" height="25%" width="20%">       
                   
                <p>2. Nutrients</p>
                   <p>Nutrients like glucose, amino acids, fatty acids,
                        vitamins, and minerals, absorbed from the digestive tract, 
                        are distributed throughout the body by the blood.</p>
                        <img src="image/nutrients.jpg" alt="Nutrients" height="25%" width="25%">      
         
    
                <p>3. Waste Products</p>
                   <p>Blood removes waste products such as urea and ammonia by
                        transporting them to the kidneys for excretion as urine.
                        It also carries carbon dioxide to the lungs for elimination.</p>
                        <img src="image/waste.jpg" alt="Waste Products" height="35%" width="25%">     
                    
           
    
                 <p>4. Other Substances</p>
                    
                    <p>Blood is essential for delivering various substances within the body.
                        Medications and hormones, after entering the bloodstream, reach their 
                        specific targets through blood circulation.</p>
                        <img src="image/4 (1).jpg" alt="Other Substances" height="35%" width="25%">    
                   
       
            <h2>B. Regulation</h2>
                <p>1. Homeostasis</p>
                      <p>Blood plays a vital role in maintaining stable internal conditions, known as homeostasis. It helps regulate:
                        <p> - Body Temperature: Distributes heat throughout the body.</p>
                        <p> - pH Balance: Maintains acid-base balance.</p>
                        <p> - Blood Pressure: Ensures consistent blood flow and pressure.</p>
                        <img src="image/Physiological-Homeostasis-768x768.jpg" alt="Homeostasis" height="45%" width="25%">   
                    </p>
                    
                    
            <h2>C. Protection</h2>
                <p>1. Immune Defense</p>
                <p>White blood cells in the blood protect against infections by engulfing pathogens 
                        through phagocytosis. Antibodies and antitoxins in the blood also neutralize harmful microbes.</p>
                <img src="image/Primary-immune-response-in-the-BM-Antigen-is-transported-via-the-blood-vessels.png" alt="Immune Defense"height="35%" width="25%">        
                  
                <p>2. Blood Clotting</p>
                    <p>Blood prevents excessive bleeding by forming clots. Platelets and clotting factors work
                        together to seal wounds and stop bleeding.</p>
