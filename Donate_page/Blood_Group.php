            <h2>Compatibility Of Blood Donor And Recipient</h2>
            <p>During a blood transfusion red blood cells obtained from a donor is 
                given to a vein (a blood vessel that carry blood towards the heart)
                of the patient or recipient through a blood transfusion set. After
                the transfusion the donor’s red cells dissolves in the recipient’s
                plasma and circulates all over the recipient’s body. Since our 
                immune system is on constant surveillance on the foreign substances
                entering into the blood circulation transfused red cells are also 
                subjected to this surveillance. The immuno-globulins or antibodies
                in the recipient’s plasma would react against the antigens of donor
                red cells if they contain a foreign antigen.</p> 
        
