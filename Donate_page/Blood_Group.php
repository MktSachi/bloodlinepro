                    <li>Universal Donors and Recipients</li>
                        <dl>
                            <dt> 1. O- </dt>
                                <dd> Universal donor for red blood cells as it has no A, B, or Rh antigens.</dd>
                            <dt>2. AB+</dt>
                                <dd> Universal recipient for red blood cells as it lacks anti-A, anti-B, and anti-Rh antibodies.</dd>
                        </dl>
           
