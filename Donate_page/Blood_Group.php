            <h3>Where there is an antigen there should be an antibody</h3>
            <p>The immune system recognizes foreign antigens on invading pathogens, producing antibodies
                to neutralize them. Immunization enhances antibody production preemptively. Interestingly, 
                the immune system refrains from producing antibodies against antigens inherited on red cells, 
                leading to the production of A and B antibodies against similar structures encountered
                in daily life.</p>
            
