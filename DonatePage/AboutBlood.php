        </div>
        <p style="text-align:justify;">Blood is a complex substance composed of several key components: red cells, white cells, platelets, and plasma. Each component has a specific function
          and shelf life. For instance, red cells, essential for transporting oxygen, can be stored for 35 to 42 days. Platelets, which are crucial for blood clotting,
